  <h2><span>Грязелечение — история</span></h2>
<p>
<br><strong>Древнеегипетское грязелечение</strong> описано в манускриптах, датированных II столетием до нашей эры. Европа знакома с технологией лечения грязями более 3 тысячелетий. Известно, что в Римской империи для заживления ран, полученных бойцами на поле боя, использовалась вулканическая грязь – природный антисептик, ускоряющий регенерацию тканей.
